package by.training.codeparser.composite;

/**
 * The enum Composite type.
 */
public enum CompositeType {
    /**
     * Package composite type.
     */
    PACKAGE, /**
     * External entity composite type.
     */
    EXTERNAL_ENTITY, /**
     * Entity composite type.
     */
    ENTITY, /**
     * Class composite type.
     */
    CLASS, /**
     * Annotation and interface composite type.
     */
    ANNOTATION_AND_INTERFACE, /**
     * Enum composite type.
     */
    ENUM, /**
     * Entity fields composite type.
     */
    ENTITY_FIELDS, /**
     * Enum constants composite type.
     */
    ENUM_CONSTANTS, /**
     * Method composite type.
     */
    METHOD, /**
     * Block composite type.
     */
    BLOCK, /**
     * Signature composite type.
     */
    SIGNATURE, /**
     * Operator composite type.
     */
    OPERATOR, /**
     * Lexeme composite type.
     */
    LEXEME
}
